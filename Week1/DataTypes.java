public class DataTypes 
{
    public static void main(String[] args) 
    {
        //List of different data types
        int age = 100;
        char c = 'A';
        double d = 3D; //Literal characters (d/D, f/F, l/L) used to force something to be for example "f/F" for float
        float f = 1000f;
        byte b1 = (byte)10;
        byte b2 = (byte)20;
        short s = (short) (b1 + b2);
    }

}
